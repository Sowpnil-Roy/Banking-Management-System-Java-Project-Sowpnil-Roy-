    private void SearchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBTNActionPerformed
        
        String query = "select * from Customer where ACNo = ?";
        String AccountNo = AccNo.getText();
        try {
            st = Database_Connection.getConnectoin().prepareStatement(query);
            st.setString(1, AccountNo);
            rs = st.executeQuery();
            if(rs.next())
            {
                String add1 = rs.getString("ACTitle");
                TitleField.setText(add1);
                String add2 = rs.getString("ContactNo");
                ContactNoField.setText(add2);
                String add3 = rs.getString("Balance");
                CurrBalField.setText(add3);
                rs.close();
                st.close();
            }else{
                JOptionPane.showMessageDialog(null, "Enter correct A/C No.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try {
                rs.close();
                st.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        
    }
    }//GEN-LAST:event_SearchBTNActionPerformed

    private void DepositBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepositBTNActionPerformed
        String currentBalance = CurrBalField.getText();
        String depositAmount = DepAmoField.getText();
        float totalBalance = Float.parseFloat(currentBalance)+ Float.parseFloat(depositAmount);
        String AccountNo = AccNo.getText();
        String query = "update customer set Balance = ' "+totalBalance+"' where ACNo = '"+AccountNo+"'";
        
        
        try {
             st = Database_Connection.getConnectoin().prepareStatement(query);
            
            st.execute();
            JOptionPane.showMessageDialog(null, "Successfully deposited.\nCurrent Balance is = Tk."+totalBalance,"Deposit Message",2);
            AccNo.setText("");
            TitleField.setText("");
            ContactNoField.setText("");
            CurrBalField.setText("");
            DepAmoField.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_DepositBTNActionPerformed

