    private void PL_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PL_btnMouseClicked
        String acc = PL_TF_AccNo.getText();
        String contNo = PL_TF_ContNo.getText();
        
        // create a select query to check if the username and the password exist in the database
        if(acc.trim().equals("") || contNo .trim().equals("") ){
            JOptionPane.showMessageDialog(null, "Please fill the required fields.","Login error",2);
        }else{
        String query = "SELECT * FROM `customer`WHERE `ACNo` = ? AND `ContactNo`= ? ";
        try {
            st = Database_Connection.getConnectoin().prepareStatement(query);
            
            st.setString(1, acc);
            st.setString(2, contNo);
            rs = st.executeQuery();
            
            if (rs.next()){
                //remove panel
        jPanel3.removeAll();
        jPanel3.repaint();
        jPanel3.revalidate();
        
        // add panel
        jPanel3.add(PL_Panel);
        jPanel3.repaint();
        jPanel3.revalidate();
        
        PL_TF_AccNo.setText("");
        PL_TF_ContNo.setText("");
        
        
        PL_AccNo.setText(acc);
        
        String Title = rs.getString("ACTitle");
        PL_AccTitle.setText(Title);
        
        PL_ContNo.setText(contNo);
        
        String CurrentBalance = rs.getString("Balance");
        PL_Balance.setText(CurrentBalance);
        
         }else{
                //Show error message
                JOptionPane.showMessageDialog(null, "Invalid A/C No or Contact No.","Login error",2);
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_PL_btnMouseClicked

