        public boolean verifyAccountNo (String AccountNo)
    {
        PreparedStatement st;
        ResultSet rs;
        boolean AccountNo_exist = false;
        String query = "SELECT * FROM `customer` WHERE `ACNo` = ? ";
        
        try {
            st = Database_Connection.getConnectoin().prepareStatement(query);
            st.setString(1, AccountNo);
            rs = st.executeQuery();
            
            if(rs.next())
            {
                AccountNo_exist = true;
                 JOptionPane.showMessageDialog(null, "This account number is already existed.", "Error",2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegisterFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return AccountNo_exist;
        
    }
    
